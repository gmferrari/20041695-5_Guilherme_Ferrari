<h1>Quem Somos</h1>
	
 <p>
Nossa empresa nasceu de um sonho de se criar doces deliciosos e vendê-los a preços acessíveis a todos.<br> Iniciamos nosso negócio em 2018 depois de participar maior concurso de doces de todos os tempos e buscamos trazer alegria e sabor de infância à <br>adultos e crianças.<br><br>

Sempre preocupados em servir bem, trazemos sempre novidades e pedidos dos clientes. Pouco a pouco nossa loja foi crescendo, e o espaço inicial, <br> 
tornou-se pequeno para tanta variedade. Precisaram de um novo espaço, muito maior que o primeiro, mudamos em 2021 então para o prédio proprio.<br>
Hoje estamos à disposição de todos na rua Amazonas nº 1000, vila Cruzeiro.<br><br>Então, se precisar de um docinho pra comer em casa,
com preço justo e confiança, visite nossa loja, será um grande prazer atendê-lo.
